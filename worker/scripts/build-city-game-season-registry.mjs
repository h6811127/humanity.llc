/**
 * Build worker season registry + public seasons index from site/data/city-game-*.json
 *
 *   npm run city-game:build-registry
 *
 * Outputs:
 *   worker/src/city-game/season-registry.generated.ts
 *   site/data/city-game-seasons-index.json
 */
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import {
  SEASON_INDEX_BASENAME,
  listSeasonJsonBasenames,
  loadSeasonJsonFile,
  seasonIndexEntryFromConfig,
} from "../../site/js/city-game-season-path-core.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "../..");
const outTs = join(root, "worker/src/city-game/season-registry.generated.ts");
const outIndex = join(root, "site/data", SEASON_INDEX_BASENAME);

function renderRegistryModule(basenames) {
  const imports = basenames
    .map(
      (name, i) =>
        `import season${i} from "../../../site/data/${name}";`
    )
    .join("\n");

  const array = basenames.map((_, i) => `season${i} as CrSeasonConfig`).join(", ");

  return `/** Generated by npm run city-game:build-registry — do not edit. */
import type { CrSeasonConfig } from "./season-config";
${imports || "// no season JSON files found"}

export const BUNDLED_SEASON_CONFIGS: CrSeasonConfig[] = [${array}];
`;
}

function main() {
  const basenames = listSeasonJsonBasenames(root);
  if (!basenames.length) {
    throw new Error("No site/data/city-game-*.json season configs found.");
  }

  mkdirSync(dirname(outTs), { recursive: true });
  writeFileSync(outTs, renderRegistryModule(basenames), "utf8");

  const seasons = basenames.map((name) => {
    const config = loadSeasonJsonFile(root, name);
    if (!config.season_id) {
      throw new Error(`${name}: missing season_id`);
    }
    return seasonIndexEntryFromConfig(config, name);
  });

  writeFileSync(
    outIndex,
    `${JSON.stringify({ version: 1, generated_at: new Date().toISOString(), seasons }, null, 2)}\n`,
    "utf8"
  );

  console.log("City game season registry\n");
  console.log("Wrote:", outTs.replace(`${root}/`, ""));
  console.log("Wrote:", outIndex.replace(`${root}/`, ""));
  for (const row of seasons) {
    console.log(`  · ${row.season_id} → ${row.rules_path ?? "(no rules page)"}`);
  }
}

main();
