/**
 * Sync Cloudflare Pages splat rewrites for discovery pin share URLs.
 * One `/discover/{region}/pin/*` rule per region — double splat does not work on Pages.
 * @see docs/DISCOVERY_PROJECTION.md · WS-DISCOVER-P1-3
 */

export const DISCOVER_PIN_SPLAT_BEGIN = "# DISCOVER_PIN_SPLAT_BEGIN";
export const DISCOVER_PIN_SPLAT_END = "# DISCOVER_PIN_SPLAT_END";

/**
 * @param {string[]} regions
 */
export function buildDiscoverPinRedirectLines(regions) {
  return [...regions]
    .map((region) => String(region ?? "").trim())
    .filter(Boolean)
    .sort()
    .map((region) => `/discover/${region}/pin/*  /discover/pin/  200`);
}

/**
 * @param {string[]} regions
 */
export function renderDiscoverPinRedirectBlock(regions) {
  const lines = buildDiscoverPinRedirectLines(regions);
  return [
    DISCOVER_PIN_SPLAT_BEGIN,
    "# Discovery pin share URLs — one splat per region (double splat does not work on Pages).",
    "# Regenerated by npm run discover:rebuild-pins from site/data/discovery-*.json",
    ...lines,
    DISCOVER_PIN_SPLAT_END,
  ].join("\n");
}

/**
 * @param {string} content
 * @param {string[]} regions
 */
export function syncDiscoverPinRedirectsInFile(content, regions) {
  const block = renderDiscoverPinRedirectBlock(regions);
  const begin = content.indexOf(DISCOVER_PIN_SPLAT_BEGIN);
  const end = content.indexOf(DISCOVER_PIN_SPLAT_END);

  if (begin !== -1 && end !== -1 && end > begin) {
    const afterEnd = content.indexOf("\n", end);
    const tail = afterEnd === -1 ? "" : content.slice(afterEnd + 1);
    const head = content.slice(0, begin).replace(/\n?$/, "");
    return tail ? `${head}\n${block}\n${tail}` : `${head}\n${block}\n`;
  }

  const legacy = content.match(
    /# Discovery pin share URLs[\s\S]*?(?=\/|$|\n# [^D]|\n\/[^d])/
  );
  if (legacy) {
    return content.replace(legacy[0], `${block}\n`);
  }

  return `${content.replace(/\n?$/, "")}\n${block}\n`;
}

/**
 * @param {string} content
 */
export function extractDiscoverPinRedirectRegions(content) {
  const lines = buildDiscoverPinRedirectLines([]);
  /** @type {string[]} */
  const regions = [];
  for (const line of content.split("\n")) {
    const match = line.match(/^\/discover\/([^/]+)\/pin\/\*\s+\/discover\/pin\/\s+200\s*$/);
    if (match) regions.push(match[1]);
  }
  return [...new Set(regions)].sort();
}

/**
 * @param {string} content
 * @param {string[]} expectedRegions
 */
export function discoverPinRedirectsInSync(content, expectedRegions) {
  const actual = extractDiscoverPinRedirectRegions(content);
  const expected = [...expectedRegions].map((r) => String(r).trim()).filter(Boolean).sort();
  if (actual.length !== expected.length) return false;
  return actual.every((region, i) => region === expected[i]);
}

/**
 * @param {string} filename
 */
export function slugFromDiscoveryIndexFilename(filename) {
  const base = String(filename ?? "").split("/").pop() ?? "";
  const match = base.match(/^discovery-(.+)\.json$/);
  return match ? match[1] : null;
}

/**
 * @param {Array<{ region?: string; filename?: string }>} indexes
 */
export function discoveryRegionsFromPinIndexes(indexes) {
  /** @type {Set<string>} */
  const regions = new Set();
  for (const row of indexes) {
    const fromField = String(row.region ?? "").trim();
    if (fromField) {
      regions.add(fromField);
      continue;
    }
    const fromName = slugFromDiscoveryIndexFilename(row.filename ?? "");
    if (fromName) regions.add(fromName);
  }
  return [...regions].sort();
}
